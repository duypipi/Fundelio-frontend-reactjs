// Generate weekly pledged data for charts
export const generateWeeklyPledgedData = (campaigns) => {
  const weeks = 7;
  const weeklyData = [];
  const now = new Date();

  for (let i = weeks - 1; i >= 0; i--) {
    const weekStart = new Date(now.getFullYear(), now.getMonth(), now.getDate() - i * 7);
    const weekEnd = new Date(weekStart);
    weekEnd.setDate(weekEnd.getDate() + 6);

    let totalPledged = 0;
    campaigns.forEach((campaign) => {
      const createdDate = new Date(campaign.createdAt);
      if (createdDate <= weekEnd) {
        // Simulate progressive pledging over time
        const campaignAge = Math.floor((weekEnd - createdDate) / (1000 * 60 * 60 * 24));
        const totalDuration = Math.floor(
          (new Date(campaign.endDate) - createdDate) / (1000 * 60 * 60 * 24)
        );
        const progress = Math.min(1, campaignAge / totalDuration);
        totalPledged += campaign.pledgedAmount * progress * 0.8; // 80% of current pledged
      }
    });

    weeklyData.push({
      week: `Tuần ${weeks - i}`,
      amount: Math.round(totalPledged),
    });
  }

  return weeklyData;
};

